#!/bin/bash

############################################################
# FUNCTION                                                 
############################################################

spotlight() {

	if [[ "$2" == "--enable" ]] || [[ "$2" == "-e" ]]; then
		mdutil -i on /
	elif [[ "$2" == "--disable" ]] || [[ "$2" == "-d" ]]; then
		mdutil -i off /
	else
		mdutil ${@:2}
	fi

}

                                              
